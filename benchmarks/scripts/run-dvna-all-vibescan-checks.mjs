#!/usr/bin/env node
import { spawnSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..", "..");

const dvnaCatalogPath = join(repoRoot, "results", "dvna-case-catalog.json");
const frameworkCatalogPath = join(repoRoot, "results", "framework-vuln-case-catalog.json");
const frameworkHighVolumeCatalogPath = join(repoRoot, "results", "framework-vuln-case-catalog-high-volume.json");
const outPath = join(repoRoot, "results", "vibescan-expanded-detection-matrix.json");
const dvnaOutDir = join(repoRoot, "benchmarks", "results", "ci_dvna_recall_baseline");
const frameworkOutDir = join(repoRoot, "benchmarks", "results", "ci_framework_vulns_vibescan");

function normRel(p) {
  return String(p || "").replace(/\\/g, "/").toLowerCase();
}

function runNodeScript(script, args = []) {
  const r = spawnSync(process.execPath, [script, ...args], {
    cwd: repoRoot,
    encoding: "utf8",
    maxBuffer: 64 * 1024 * 1024,
    env: process.env,
  });
  if (r.status !== 0) {
    const msg = r.stderr || r.stdout || `${script} exited ${r.status}`;
    throw new Error(msg);
  }
}

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

function hitByAnchor(finding, c) {
  const filePath = normRel(finding.filePath || finding.file || "");
  if (!filePath.endsWith(normRel(c.anchorSuffix))) return false;
  const line = Number(finding.line);
  return (c.anchorLines || []).some((n) => Number(n) === line);
}

function scoreDvnaCases(findings, catalog) {
  const detections = {};
  for (const c of catalog.cases || []) {
    detections[c.id] = findings.some((f) => hitByAnchor(f, c));
  }
  return detections;
}

function scoreRuleCases(findings, catalog) {
  const detections = {};
  for (const c of catalog.cases || []) {
    const expected = new Set(c.expectedRuleIds || []);
    detections[c.id] = findings.some((f) => hitByAnchor(f, c) && expected.has(f.ruleId));
  }
  return detections;
}

function countHits(map) {
  const vals = Object.values(map);
  return vals.filter((v) => v === true).length;
}

function parseArgs(argv) {
  return {
    failOnMiss: !argv.includes("--no-fail-on-miss"),
  };
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  mkdirSync(dvnaOutDir, { recursive: true });
  mkdirSync(frameworkOutDir, { recursive: true });

  runNodeScript(join(repoRoot, "benchmarks", "scripts", "run-dvna-vibescan-scan.mjs"), ["--out-dir", dvnaOutDir]);
  runNodeScript(join(repoRoot, "benchmarks", "scripts", "run-framework-vuln-scan.mjs"), ["--out-dir", frameworkOutDir]);

  const dvnaCatalog = readJson(dvnaCatalogPath);
  const ruleCatalog = readJson(frameworkCatalogPath);
  const ruleCatalogHighVolume = readJson(frameworkHighVolumeCatalogPath);
  const allRuleCases = [...(ruleCatalog.cases || []), ...(ruleCatalogHighVolume.cases || [])];
  const dvnaProject = readJson(join(dvnaOutDir, "vibescan-project.json"));
  const frameworkProject = readJson(join(frameworkOutDir, "vibescan-project.json"));
  const dvnaDetections = scoreDvnaCases(dvnaProject.findings || [], dvnaCatalog);
  const ruleDetections = scoreRuleCases(frameworkProject.findings || [], { cases: allRuleCases });

  const dvnaHits = countHits(dvnaDetections);
  const dvnaTotal = Object.keys(dvnaDetections).length;
  const ruleHits = countHits(ruleDetections);
  const ruleTotal = Object.keys(ruleDetections).length;
  const totalHits = dvnaHits + ruleHits;
  const totalRows = dvnaTotal + ruleTotal;

  const payload = {
    version: 1,
    source: "Generated by benchmarks/scripts/run-dvna-all-vibescan-checks.mjs",
    scope: "DVNA anchors + expanded all-rule corpus anchors",
    rows: {
      dvna: { total: dvnaTotal, hits: dvnaHits, detections: dvnaDetections },
      allChecks: { total: ruleTotal, hits: ruleHits, detections: ruleDetections },
      combined: { total: totalRows, hits: totalHits, recall: Number((totalHits / totalRows).toFixed(4)) },
    },
  };

  writeFileSync(outPath, `${JSON.stringify(payload, null, 2)}\n`, "utf8");
  console.log(`Wrote ${outPath}`);
  console.log(`DVNA: ${dvnaHits}/${dvnaTotal}`);
  console.log(`All checks corpus: ${ruleHits}/${ruleTotal}`);
  console.log(`Combined: ${totalHits}/${totalRows}`);

  if (args.failOnMiss && (dvnaHits < dvnaTotal || ruleHits < ruleTotal)) {
    process.exit(1);
  }
}

main();
